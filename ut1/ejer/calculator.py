number1 = int(input("Give me first number: "))
number2 = int(input("Give me second number: "))

result = number1 + number2
print(number1, number2, sep="+", end="=")
print(result)

result = number1 - number2
print(number1, number2, sep="-", end="=")
print(result)

result = number1 * number2
print(number1, number2, sep="*", end="=")
print(result)

result = number1 / number2
print(number1, number2, sep="/", end="=")
print(result)